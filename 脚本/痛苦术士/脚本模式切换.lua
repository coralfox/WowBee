-- 副本模式
BeeSetVariable("combat_toggle", true)
-- true为自动进入战斗状态,false为手动进入战斗状态

BeeSetVariable("tar_combat_toggle", false)
--  true为目标是否进入战斗状态都自动攻击,false为敌方进入战斗状态才会攻击

BeeSetVariable("pvp_toggle", false)
-- true为敌对玩家时执行,false为敌对玩家时不执行

BeeSetVariable("mount_toggle", false)
-- true为骑马执行,false为骑马不执行

BeeSetVariable("tab_toggle", true)
-- true为自动切换目标,false为手动tab切换目标

BeeSetVariable("key_toggle", false)
-- true为按下修饰键(Alt,Shift,Ctrl)时执行,false为不需要辅助键执行

BeeSetVariable("kz_toggle", false)
-- true为近身则恐惧,false为不执行恐惧法术

BeeSetVariable("fs_spell", "腐蚀之种")
-- 指定的腐蚀法术

